// ── Fonctions d'interpolation (validées par Ronde #1 + #2, consensus 3/3) ──

/** Décode un Uint16 quantifié en degrés */
function qToDeg(q: number): number {
  return (q * 360) / 65535;
}

/** Normalise un angle dans [0, 360) */
function norm360(x: number): number {
  x %= 360;
  return x < 0 ? x + 360 : x;
}

/**
 * Distance angulaire la plus courte entre deux angles.
 * Bullet-proof en JS (gère le modulo négatif).
 * Retourne un résultat dans (-180, 180].
 *
 * Validé Ronde #2 : formule ((diff+540)%360)-180 + guard 180°
 * Sources : GPT (cas antipodal), Gemini (modulo JS)
 */
function shortAngleDist(a0: number, a1: number): number {
  let d = ((a1 - a0 + 540) % 360) - 180;
  if (d === -180) d = 180; // cas antipodal déterministe
  return d;
}

/**
 * Interpolation linéaire angulaire (pour les bords de table).
 * Validé Ronde #2 : GPT recommande linéaire aux extrémités.
 */
function lerpAngle(a: number, b: number, t: number): number {
  return norm360(a + shortAngleDist(a, b) * t);
}

/**
 * Interpolation Catmull-Rom adaptée aux angles circulaires.
 * Gère le wrap-around 359°→0° via déroulement relatif.
 *
 * Validé Ronde #1 (Gemini) + corrigé Ronde #2 :
 * - Déroulement directionnel c0 = y1 - da01 (Grok)
 * - shortAngleDist bullet-proof (GPT + Gemini)
 */
function catmullRomAngle(y0: number, y1: number, y2: number, y3: number, t: number): number {
  const da01 = shortAngleDist(y0, y1);
  const da12 = shortAngleDist(y1, y2);
  const da23 = shortAngleDist(y2, y3);

  const c0 = y1 - da01;
  const c1 = y1;
  const c2 = y1 + da12;
  const c3 = c2 + da23;

  // Tangentes Catmull-Rom
  const m0 = (c2 - c0) * 0.5;
  const m1 = (c3 - c1) * 0.5;

  // Polynôme cubique Hermite
  const t2 = t * t, t3 = t2 * t;
  const result = (2 * t3 - 3 * t2 + 1) * c1
               + (t3 - 2 * t2 + t) * m0
               + (-2 * t3 + 3 * t2) * c2
               + (t3 - t2) * m1;

  return norm360(result);
}

/**
 * Calcule la longitude écliptique géocentrique tropicale de Chiron
 * à partir de la table d'éphémérides.
 *
 * @param dd — jours depuis J2000.0 (1er janvier 2000, 12:00 TT)
 * @returns longitude 0-360, ou null si la table n'est pas chargée
 */
export function calcChironFromTable(dd: number): number | null {
  if (!CHIRON_Q) return null;

  const x = (dd - CHIRON_START_DD) * 0.1; // ×0.1 au lieu de /10 (micro-optim Gemini)
  const last = CHIRON_Q.length - 1;

  // Clamp aux bords (pas de crash — Ronde #2 Grok)
  if (x <= 0) return qToDeg(CHIRON_Q[0]);
  if (x >= last) return qToDeg(CHIRON_Q[last]);

  const i = Math.floor(x);
  const t = x - i;

  const p1 = qToDeg(CHIRON_Q[i]);
  const p2 = qToDeg(CHIRON_Q[Math.min(last, i + 1)]);

  // Bords : interpolation linéaire (Ronde #2 — GPT)
  // Catmull-Rom nécessite 4 points propres → linéaire pour i<1 ou i>last-2
  if (i < 1 || i > last - 2) {
    return lerpAngle(p1, p2, t);
  }

  // Intérieur : Catmull-Rom angulaire (Ronde #1 — Gemini)
  const p0 = qToDeg(CHIRON_Q[i - 1]);
  const p3 = qToDeg(CHIRON_Q[i + 2]);

  return catmullRomAngle(p0, p1, p2, p3, t);
}
